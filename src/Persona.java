public class Persona {

    private String nom;
    private String DNI;

    public Persona(String nom) {
        this.nom = nom;
    }












    //GETTERS && SETTERS


    public String getNom() {
        return nom;
    }

    public void setNom(String nom) {
        this.nom = nom;
    }

    public String getDNI() {
        return DNI;
    }

    public void setDNI(String DNI) {
        this.DNI = DNI;
    }
}
